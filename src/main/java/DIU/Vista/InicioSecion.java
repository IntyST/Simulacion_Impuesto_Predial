    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        // Crear una instancia de usuarioControlador
        usuarioControlador controlador = new usuarioControlador();

        // Obtener el correo y la contraseña ingresados por el usuario
        String correo = txtCorreo.getText();
        String contrasenia = new String(txtContrasenia.getPassword());

        // Llamar al método validarLogin
        PersonaModelo persona = controlador.validarLogin(correo, contrasenia);

        // Si las credenciales son válidas, abrir la ventana PantallaAdministrador
        if (persona != null) {
            PantallaAdministrador pantallaAdmin = new PantallaAdministrador(persona);
            pantallaAdmin.setVisible(true);
            this.dispose();  // Cerrar la ventana InicioSecion
        } 
    }//GEN-LAST:event_btnAccederActionPerformed

